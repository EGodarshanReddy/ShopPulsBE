import * as React from "react"

import { cn } from "@/lib/utils"

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & {
    variant?: "default" | "glass" | "gradient" | "hover" | "outlined"
  }
>(({ className, variant = "default", ...props }, ref) => (
  <div
    ref={ref}
    className={cn(
      "rounded-xl border bg-card text-card-foreground shadow transition-all duration-200", 
      variant === "glass" && "glass-card backdrop-blur-md border-white/10",
      variant === "gradient" && "gradient-border bg-card/95",
      variant === "hover" && "card-hover hover:border-primary/30",
      variant === "outlined" && "border-2",
      className
    )}
    {...props}
  />
))
Card.displayName = "Card"

